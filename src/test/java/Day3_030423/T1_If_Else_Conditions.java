package Day3_030423;

public class T1_If_Else_Conditions {

    public static void main(String[] args) {

        //print when a is less than b
        int a = 3;
        int b = 2;

        //any command that has an opening {, if you hit enter on your keypad it will create it for you
        if (a<b) {
            System.out.println("a is less than b");
        }else {
            System.out.println("a is greater than or equal to b");
        }//end of conditional statements

        int c = 4;
        int d = 7;

        if(c<d) {
            System.out.println("c is less than d");
        }
        if(d>c) {
            System.out.println("d is greater than c");
        }



}//end of main
} //end of java class
